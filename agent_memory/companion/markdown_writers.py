# ─── V3-O.7 Round B: 補缺 writer ─────────────────────────────────────

def write_trait_evolution_md(
    vault_root: Path,
    *,
    trait_name: str,
    old_value: float,
    new_value: float,
    delta: float,
    evidence_count: int,
    trigger: str = "drift_guard_confirm",
    user_id: str = "",
    evolution_id: Optional[str] = None,
) -> Optional[Path]:
    """V3-O.7 Round B: 性格特質演化 → 30_Emotional_State/33_Trait_Evolution/<trait>.md.

    drift_guard 確認更新後呼叫，記錄慢速性格變化歷史軌跡 (append per trait).
    """
    import uuid as _uuid
    eid = evolution_id or _uuid.uuid4().hex[:12]
    now = datetime.now(timezone.utc).isoformat()
    safe_name = _safe_filename(trait_name)
    target = vault_root / TRAIT_EVOLUTION_DIR / f"{safe_name}.md"
    target.parent.mkdir(parents=True, exist_ok=True)

    entry_lines = [
        "",
        f"## {now[:19]} — {eid}",
        "",
        f"- trait: {trait_name}",
        f"- old_value: {old_value:.4f}",
        f"- new_value: {new_value:.4f}",
        f"- delta: {delta:+.4f}",
        f"- evidence_count: {evidence_count}",
        f"- trigger: {trigger}",
        f"- user_id: {user_id}",
        "",
    ]

    if not target.exists():
        header = "\n".join([
            "---",
            "type: trait_evolution",
            "schema_version: 10",
            f"trait_name: {trait_name}",
            f"created_at: {now}",
            "---",
            f"# 性格演化 — {trait_name}",
        ])
        content = header + "\n" + "\n".join(entry_lines) + "\n"
    else:
        content = target.read_text(encoding="utf-8") + "\n".join(entry_lines) + "\n"

    try:
        atomic_write(target, content)
        return target
    except Exception:
        return None


