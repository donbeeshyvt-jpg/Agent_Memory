# ─── F3: Drift Guard 候選 markdown ───────────────────────────────────
def write_drift_candidate_md(
    vault_root: Path,
    *,
    trait_name: str,
    proposed_value: float,
    evidence_count: int,
    drift_score: float,
    evidence_event_ids: Optional[list[str]] = None,
    current_value: Optional[float] = None,
    user_id: str = "",
    candidate_id: Optional[str] = None,
) -> Optional[Path]:
    """V3-G6 F3 + V3-H2 殘-04: 統一 drift_candidate markdown writer.

    V3-H2 (user 2026-05-27 殘-04): 統一 drift_guard 既有 atomic_write 路徑.
    Schema superset 涵蓋 drift_guard 原欄位 (current_value/user_id/awaiting_human_confirm).
    """
    evidence_event_ids = evidence_event_ids or []
    now = datetime.now(timezone.utc).isoformat()
    safe_name = _safe_filename(trait_name)
    cid = candidate_id or now[:19].replace(":", "-")
    target = vault_root / PERSONA_CANDIDATE_DIR / f"{safe_name}_{cid}.md"
    target.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "---",
        "type: persona_version_candidate",
        "schema_version: 10",
    ]
    if user_id:
        lines.append(f'user_id: "{user_id}"')
    lines.append(f"trait_name: {trait_name}")
    lines.append(f"proposed_value: {proposed_value:.4f}")
    if current_value is not None:
        lines.append(f"current_value: {current_value:.4f}")
    lines.extend([
        f"evidence_count: {evidence_count}",
        f"drift_score: {drift_score:.4f}",
        "awaiting_active: true",
        "awaiting_human_confirm: true",  # backward compat with drift_guard 原 schema
        "active: false",
        f"created_at: {now}",
        "---",
        f"# Drift Guard 候選 — {trait_name}",
        "",
        f"建議新 value: **{proposed_value:.4f}** (drift={drift_score:.2f}, evidence={evidence_count})",
    ])
    if current_value is not None:
        lines.append(f"目前 value: {current_value:.4f}")
    lines.extend(["", "## Evidence (raw_events)", ""])
    # ⭐ V3-N (user 2026-05-27): 加 [[wikilinks]] 連回 event MD
    for eid in evidence_event_ids[:10]:
        lines.append(f"- [[{eid}]]")
    lines.extend([
        "",
        "## user 動作 (對齊 V3 §22 Drift Guard 紅線)",
        "",
        "1. 中之人 review 內容",
        "2. 如同意 → 改 frontmatter `awaiting_active: false` / `awaiting_human_confirm: false` + `active: true`",
        "3. 系統下次重啟 → 此 trait 漂移生效",
        "",
        f"*Auto-generated by markdown_writers.py V3-G6/H2 ({now[:19]})*",
    ])
    try:
        atomic_write(target, "\n".join(lines) + "\n")
        return target
    except Exception:
        return None


